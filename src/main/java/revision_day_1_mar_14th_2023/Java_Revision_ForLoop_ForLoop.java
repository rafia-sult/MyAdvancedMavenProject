package revision_day_1_mar_14th_2023;

public class Java_Revision_ForLoop_ForLoop {

	public static void main(String[] args) {
		for (int i = 1; i <= 3; i++) {
			for (int j=1; j<=i; j++) {
				System.out.println("Hello");
			}
			
			System.out.println();
		}
		
		
		for (int i = 1; i <= 3; i++) {
			for (int j=1; j<=4; j++) {
				System.out.println("Hello");
			}
			
			System.out.println();
		}
	}

}
